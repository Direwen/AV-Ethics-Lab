func SeedContextTemplates(repo repository.TemplateRepository) error {
	ctx := context.Background()
	for _, tpl := range templates {
		if err := repo.FirstOrCreate(ctx, &tpl); err != nil {
			return err
		}
		log.Default().Println("Seeded template: ", tpl.Name)
	}
	return nil
}
